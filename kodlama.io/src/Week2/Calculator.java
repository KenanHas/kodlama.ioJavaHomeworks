public class Calculator {

	public int Summation(int numberOne, int numberTwo) {

		return numberOne + numberTwo;
	}

	public int Substraction(int numberOne, int numberTwo) {

		return numberOne - numberTwo;
	}

	public int Multiplication(int numberOne, int numberTwo) {

		return numberOne * numberTwo;
	}

	public double Divide(int numberOne, int numberTwo) {

		return (double) numberOne / numberTwo;
	}

}
